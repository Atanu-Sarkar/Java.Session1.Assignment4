package com.acadgild.session.one;

public class Expressions {

	public static void main(String[] args) {
		
		int x =5;
		int y=10;
		//a. x + y * 2
		int z1;
		z1 = x + y * 2;
	    System.out.println(z1);
		//b. x - y + 2 
	    int z2;
	    z2 = x - y +2;
	    System.out.println(z2);
	    //c. (x+y) * 2
	    int z3;
	    z3 = (x + y) * 2;
	    System.out.println(z3);
		//d. y % x
	    int z4;
	    z4 = y % x;
	    System.out.println(z4);
	    
	}

}
